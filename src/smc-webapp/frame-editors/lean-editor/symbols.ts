interface Sub {
  from: number;
  to: number;
  replacement: string;
}

export function substitute_symbols(value: string, j: number = 0): Sub[] {
  let i = value.indexOf("\\", j);
  if (i === -1) {
    return [];
  }
  // Replace the one at i.
  const s = value.slice(i + 1);
  if (s[0] === undefined || /\s/.test(s[0])) {
    return substitute_symbols(value, i + 1);
  }
  let closest: string = "";
  for (let m in TRANSLATIONS) {
    if (s.slice(0, m.length) == m && m.length > closest.length) {
      closest = m;
    }
  }
  if (closest === "") {
    return substitute_symbols(value, i + 1);
  }
  const replacement = TRANSLATIONS[closest];
  value = value.slice(0, i) + replacement + value.slice(i + closest.length + 1);
  const from = i,
    to = i + closest.length + 1;
  const result: Sub[] = [{ replacement, from, to }];
  for (let x of substitute_symbols(value, i + 1)) {
    result.push(x);
  }
  return result;
}

